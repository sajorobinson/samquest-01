struct EntityAttributes {
    var strength: Int
    var agility: Int
    var mystique: Int
    var understanding: Int
    var endurance: Int
    var logic: Int
    
    init(
        strength: Int,
        agility: Int,
        mystique: Int,
        understanding: Int,
        endurance: Int,
        logic: Int
    ) {
        self.strength = strength
        self.agility = agility
        self.mystique = mystique
        self.understanding = understanding
        self.endurance = endurance
        self.logic = logic
    }
}

