class Location {
    
    var name: String
    
    var description: String
    
    let x: Int
    
    let y: Int
    
    init(name: String, description: String, x: Int, y: Int) {
        
        self.name = name
        
        self.description = description
        
        self.x = x
        
        self.y = y
        
    }
    
}
